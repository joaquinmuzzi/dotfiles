#!/usr/bin/env python3

from __future__ import annotations

import argparse
from pathlib import Path

CONFIG_TEMPLATE = """# Generated by generate-mangohud-conf.py
# ThinkPad X1 Carbon 14-inch optimized MangoHud profile

legacy_layout=0
horizontal
position=top-left
font_size=20

fps
frame_timing=1
cpu_stats
gpu_stats
cpu_temp
gpu_temp
ram
uptime

no_display
toggle_hud=Shift_R+F12
"""


def write_config(output_path: Path, force: bool) -> None:
    if output_path.exists() and not force:
        raise FileExistsError(
            f"{output_path} already exists. Use --force to overwrite."
        )

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(CONFIG_TEMPLATE, encoding="utf-8")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Generate a MangoHud.conf for ThinkPad X1 Carbon (14-inch)."
    )
    parser.add_argument(
        "-o",
        "--output",
        type=Path,
        default=Path("~/.config/MangoHud/MangoHud.conf").expanduser(),
        help="Output path for MangoHud.conf",
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Overwrite output file if it already exists.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()

    try:
        write_config(args.output, args.force)
    except FileExistsError as exc:
        print(f"Error: {exc}")
        return 1

    print(f"MangoHud config generated at: {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
